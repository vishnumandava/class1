public class ListExample {
	    public static void main(String[] args) {
	        
	        java.util.List<Integer> list = Arrays.asList(3,2,1,4,5,6,6);

	      
	        for (Integer integer : list) {
	            System.out.println(integer);
	        }
	    }

	}


